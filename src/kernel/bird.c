void bird_draw(enum FlapState frame) {
    for (int y = 0; y < 24; y++) {
        for (int x = 0; x < 34; x++) {
            putpixel(frame == FLAP_DOWN   ? downflap[y][x] : 
                     frame == FLAP_MIDDLE ? midflap[y][x] :
                                       upflap[y][x], 
                    100 + x, 100 + y);
        }
    }
}